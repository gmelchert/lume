"use client";

import { motion } from "framer-motion";

/** Subtle fade-in on every route navigation. Opacity-only so it never breaks sticky/fixed layout. */
export default function Template({ children }: { children: React.ReactNode }) {
  return (
    <motion.div
      initial={{ opacity: 0 }}
      animate={{ opacity: 1 }}
      transition={{ duration: 0.35, ease: [0.22, 1, 0.36, 1] }}
    >
      {children}
    </motion.div>
  );
}
